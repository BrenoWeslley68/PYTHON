c = float(input('Insira a temperatura atual aqui: '))
conversao = ((c * 9) / 5) + 32
print(f'A temperatura atual em cº é {c}, e a converção em fº {conversao}')
f = float(input('Digite o valor em fº'))
conversaof = ((f - 32) * 5) / 9
print(f'A temperatura atual em fº é {f}, e a converção em cº {conversaof}')
